// ═══════════════════════════════════════════════════════
// ESP32 WebSocket Client
// Connects to ESP32 WebSocket and stores incoming data
// ═══════════════════════════════════════════════════════
let esp32Ws = null;
let esp32Ip = null;
let esp32Connected = false; // Track connection status
let reconnectTimer = null;
let reconnectAttempts = 0;
let lastError = null;
let lastErrorTime = null;

let esp32ConnectionStartTime = null;

function connectToESP32(ip) {
  if (esp32Ws) {
    try { esp32Ws.close(); } catch {}
  }
  clearTimeout(reconnectTimer);

  esp32Ip = ip;
  const url = `ws://${ip}:81`;
  esp32ConnectionStartTime = Date.now();
  reconnectAttempts++;
  
  // Only log every 3rd attempt to reduce spam
  if (reconnectAttempts % 3 === 1) {
    console.log(`[ESP32-Bridge] Connecting to ${url}... (attempt ${reconnectAttempts})`);
  }

  esp32Ws = new WebSocket(url);

  // Timeout handler - fail if no connection after 10 seconds
  const connectionTimeout = setTimeout(() => {
    if (esp32Ws && esp32Ws.readyState !== WebSocket.OPEN) {
      console.error(`[ESP32-Bridge] ✗ Connection timeout: Unable to connect to ${url}`);
      try { esp32Ws.close(); } catch {}
      esp32Ws = null;
    }
  }, 10000);

  esp32Ws.on('open', () => {
    clearTimeout(connectionTimeout);
    esp32Connected = true; // Mark as connected
    const elapsed = Date.now() - esp32ConnectionStartTime;
    console.log(`[ESP32-Bridge] ✓ Connected to ESP32 at ${ip} (took ${elapsed}ms)`);
  });

  esp32Ws.on('message', async (raw) => {
    try {
      const data = JSON.parse(raw.toString());
      setLatestSensorReading({
        weight: data.weight ?? null,
        load_weight: data.load_weight ?? null,
        water_level: data.water_level ?? null,
        turbidity: data.turbidity ?? null,
        current: data.current ?? 0,
        hx711_ready: data.hx711_ready ?? null,
        pump_status: data.pump_status ?? null,
        motor_status: data.motor_status ?? null,
        temperature: data.temperature ?? null,
      });
      // All data is forwarded to SQL Server via .NET API

      if (FORWARD_TO_DOTNET) {
        // Convert to DataModel format for .NET API
        const pumpStatusStr = (data.pump_status === 1 || data.pump_status === true || data.pump_status === 'on') ? 'on' : 'off';
        const motorStatusStr = (data.motor_status === 1 || data.motor_status === true || data.motor_status === 'on') ? 'on' : 'off';
        
        const dataForDotnet = {
          sensor_id: 1,
          weight: data.weight ?? 0,
          load_weight: data.load_weight ?? 0,
          water_level: data.water_level ?? 0,
          turbidity: data.turbidity ?? 0,
          current_value: data.current ?? 0,
          hx711_ready: data.hx711_ready ?? true,
          pump_status: pumpStatusStr,
          motor_status: motorStatusStr,
          // created_time is auto-generated by SQL Server
        };
        const dotnetResult = await forwardToDotnet(dataForDotnet);
        console.log(`[DOTNET] Forwarded ESP32 reading (${dotnetResult.status}) to ${DOTNET_API_URL}`);
      }

      console.log(`[ESP32] Reading received — water:${data.water_level}% load:${data.load_weight}kg current:${data.current}A temp:${data.temperature}°C pump:${data.pump_status} motor:${data.motor_status}`);
    } catch (err) {
      console.error('[ESP32] Error handling sensor data:', err.message);
    }
  });

  esp32Ws.on('close', () => {
    esp32Connected = false; // Mark as disconnected
    console.log('[ESP32-Bridge] Connection closed');
    esp32Ws = null;
    // Auto-reconnect after 5 seconds
    if (esp32Ip) {
      reconnectTimer = setTimeout(() => connectToESP32(esp32Ip), 5000);
    }
  });

  esp32Ws.on('error', (err) => {
    clearTimeout(connectionTimeout);
    lastError = err.message;
    lastErrorTime = new Date().toISOString();
    
    // Only log unique errors (not repeats)
    if (err.message !== lastError) {
      console.error(`[ESP32-Bridge] ✗ Error (attempt ${reconnectAttempts}): ${err.message}`);
      
      if (err.message.includes('ECONNREFUSED')) {
        console.error(`  Port 81 is CLOSED or NOT LISTENING at ${ip}`);
        console.error(`  → ESP32 WebSocket server is NOT running`);
        console.error(`  → Check: Is ESP32 powered? Is firmware running? Is port 81 correct?`);
      } else if (err.message.includes('ETIMEDOUT')) {
        console.error(`  Connection TIMED OUT (10s) - IP may be unreachable`);
        console.error(`  → Check: Can you ping ${ip}?`);
        console.error(`  → Verify: Same network? Firewall blocking port 81?`);
      } else if (err.message.includes('EHOSTUNREACH')) {
        console.error(`  Host UNREACHABLE - cannot reach ${ip}`);
        console.error(`  → Verify IP address is correct`);
        console.error(`  → Check: Same WiFi network?`);
      } else {
        console.error(`  Cannot connect to ws://${ip}:81`);
        console.error(`  1. Verify ESP32 IP address: ${ip}`);
        console.error(`  2. Ensure WebSocket server runs on port 81`);
        console.error(`  3. Check network connectivity`);
      }
    }
  });
}

app.post('/api/esp32/connect', (req, res) => {
  const { ip } = req.body;
  if (!ip) {
    return res.status(400).json({ success: false, error: 'IP address is required' });
  }
  // Validate IP format (basic check)
  const ipRegex = /^(\d{1,3}\.){3}\d{1,3}$|^localhost$/i;
  if (!ipRegex.test(ip)) {
    return res.status(400).json({ success: false, error: 'Invalid IP address format' });
  }
  connectToESP32(ip);
  res.json({ success: true, message: `Connecting to ESP32 at ${ip}...` });
});

// ═══ Endpoint to disconnect backend from ESP32 ═══
app.post('/api/esp32/disconnect', (req, res) => {
  clearTimeout(reconnectTimer);
  esp32Ip = null;
  esp32Connected = false; // Mark as disconnected
  if (esp32Ws) {
    try { esp32Ws.close(); } catch {}
    esp32Ws = null;
  }
  res.json({ success: true, message: 'Disconnected from ESP32' });
});

