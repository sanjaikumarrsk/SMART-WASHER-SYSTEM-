// ═══ Start Server ═══
async function startServer() {
  const server = app.listen(PORT, () => {
    console.log(`\n🚀 Smart Washer Backend running on http://localhost:${PORT}`);
    console.log(`   API endpoints available at http://localhost:${PORT}/api`);
    console.log(`   Forwarding ESP32 data to SQL Server via .NET API`);
    console.log(`   Endpoint: ${DOTNET_API_URL}`);
    console.log(`\n   To start ESP32 bridge, POST to /api/esp32/connect with { "ip": "ESP32_IP" }\n`);
  });

  // Error handling
  server.on('error', (err) => {
    console.error('[Server Error]', err);
  });

  process.on('uncaughtException', (err) => {
    console.error('[Uncaught Exception]', err);
  });

  process.on('unhandledRejection', (reason, promise) => {
    console.error('[Unhandled Rejection]', reason);
  });
}

startServer().catch((err) => {
  console.error('[Startup Error]', err);
  process.exit(1);
});
